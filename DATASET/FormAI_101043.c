#include <stdio.h>

int main() {
  int a = 10, b = 5;
  
  // Addition
  int sum = a + b;
  printf("Sum of %d and %d is %d\n", a, b, sum);
  
  // Subtraction
  int diff = a - b;
  printf("Difference between %d and %d is %d\n", a, b, diff);
  
  // Multiplication
  int product = a * b;
  printf("Product of %d and %d is %d\n", a, b, product);
  
  // Division
  int quotient = a / b;
  printf("Quotient of %d divided by %d is %d\n", a, b, quotient);
  
  // Modulus
  int remainder = a % b;
  printf("Remainder when %d is divided by %d is %d\n", a, b, remainder);
  
  // Increment
  int c = 5;
  int d = ++c;
  printf("c is %d and d is %d\n", c, d);
  
  // Decrement
  int e = 10;
  int f = --e;
  printf("e is %d and f is %d\n", e, f);
  
  // Bitwise AND
  int num1 = 12, num2 = 25;
  int result = num1 & num2;
  printf("Bitwise AND of %d and %d is %d\n", num1, num2, result);
  
  // Bitwise OR
  int num3 = 20, num4 = 15;
  int result2 = num3 | num4;
  printf("Bitwise OR of %d and %d is %d\n", num3, num4, result2);
  
  // Bitwise XOR
  int num5 = 3, num6 = 5;
  int result3 = num5 ^ num6;
  printf("Bitwise XOR of %d and %d is %d\n", num5, num6, result3);
  
  // Bitwise NOT
  int num7 = 10;
  int result4 = ~num7;
  printf("Bitwise NOT of %d is %d\n", num7, result4);
  
  return 0;
}