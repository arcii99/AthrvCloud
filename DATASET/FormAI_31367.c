#define H 50 /* Height of the image */
#define W 50 /* Width of the image */

char ascii[H][W]; /* Array to hold the ASCII art */

void convert(char image[H][W]) { /* Converts image to ASCII art */

  for (int i = 0; i < H; i++) {
    for (int j = 0; j < W; j++) {
      if (image[i][j] >= 0 && image[i][j] < 50) {
        ascii[i][j] = ' '; /* Dark */
      } else if (image[i][j] >= 50 && image[i][j] < 100) {
        ascii[i][j] = '.'; /* Mid-tone */
      } else {
        ascii[i][j] = '*'; /* Light */
      }
    }
  }
}

