#include <stdio.h>
#include <stdlib.h>
#include <stdint.h>
#include <time.h>

int main(void) {
    srand(time(NULL));
    int current_bandwidth = rand() % 100 + 1; // Generates a random integer between 1 and 100 inclusive
    int user_traffic, latency, packet_loss;

    printf("Welcome to the Network Quality of Service (QoS) monitor!\n");
    printf("Current bandwidth: %d Mbps\n\n", current_bandwidth);

    while(1) { // Loop forever until user chooses to exit
        printf("Please enter the amount of network traffic (in Mbps) generated by users: ");
        scanf("%d", &user_traffic);

        printf("Please enter the current latency (in milliseconds): ");
        scanf("%d", &latency);

        printf("Please enter the current packet loss (in percentage): ");
        scanf("%d", &packet_loss);

        printf("\nChecking QoS metrics...\n");
        if(user_traffic > current_bandwidth) {
            printf("WARNING: Network traffic exceeds current bandwidth!\n");
        }
        
        if(latency > 100) {
            printf("WARNING: High latency detected!\n");
        }
        
        if(packet_loss > 10) {
            printf("WARNING: High packet loss detected!\n");
        }

        // Wait for 5 seconds before checking again
        printf("\nWaiting for 5 seconds...\n");
        sleep(5);
        printf("\n");

        // Update current bandwidth randomly between 1 and 10 Mbps
        current_bandwidth = current_bandwidth - ((rand() % 10 + 1));
        printf("Current bandwidth: %d Mbps\n\n", current_bandwidth);

        // Exit loop if current bandwidth is less than or equal to 0
        if(current_bandwidth <= 0) {
            printf("ERROR: Network bandwidth is too low to continue!\n");
            break;
        }
    }

    printf("Exiting program...\n");

    return 0;
}