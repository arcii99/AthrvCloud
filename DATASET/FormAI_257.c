#include<stdio.h>

int main()
{
    int a = 10, b = 5;
    int bitwise_and = a & b;
    int bitwise_or = a | b;
    int bitwise_xor = a ^ b;
    int bitwise_not_of_a = ~a;
    int bitwise_left_shift = a << 1;
    int bitwise_right_shift = a >> 1;
    
    printf("My love, let me demonstrate my affection for you through bitwise operations.\n");

    printf("When we '&' our love together, we get: %d\n", bitwise_and);
    printf("When we '|' our love together, we get: %d\n", bitwise_or);
    printf("When we '^' our love together, we get: %d\n", bitwise_xor);
    printf("When I '~' your love, I get the complement of perfection: %d\n", bitwise_not_of_a);
    printf("When I '<<' our love, I see my future brighter with you: %d\n", bitwise_left_shift);
    printf("When I '>>' our love, I want to hold onto our precious moments forever: %d\n", bitwise_right_shift);
    
    printf("My love, our bitwise operations together show that our love is not just a simple sum, or a difference.\n");

    return 0;
}