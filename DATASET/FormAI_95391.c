#include <stdio.h>

int main () {
    int a = 23;
    int b = 12;

    // Let's perform some exciting Bitwise Operations!

    // Bitwise AND
    int c = a & b;
    printf("Result of Bitwise AND between %d and %d is: %d\n", a, b, c);

    // Bitwise OR
    int d = a | b;
    printf("Result of Bitwise OR between %d and %d is: %d\n", a, b, d);

    // Bitwise XOR
    int e = a ^ b;
    printf("Result of Bitwise XOR between %d and %d is: %d\n", a, b, e);

    // Bitwise NOT
    int f = ~a;
    printf("Result of Bitwise NOT of %d is: %d\n", a, f);

    // Left Shift
    int g = a << 2;
    printf("Result of Left Shift by 2 of %d is: %d\n", a, g);

    // Right Shift
    int h = b >> 2;
    printf("Result of Right Shift by 2 of %d is: %d\n", b, h);

    printf("\nWOW! Bitwise Operations in C are exciting!\n");

    return 0;
}