#include <stdio.h>

int main()
{
    float celsius, fahrenheit;

    printf("Enter temperature in Celsius: ");
    scanf("%f", &celsius);

    fahrenheit = (celsius * 9 / 5) + 32;

    printf("\nTemperature in Fahrenheit: %.2f\n", fahrenheit);

    printf("\n");
    printf("                 ###############\n");
    printf("        -==-   ##           ##\n");
    printf("    --   /\\    ##           ##\n");
    printf("         /  \\   ##           ##\n");
    printf("        |    |  ##           ##\n");
    printf("         \\__/   ##           ##\n");
    printf("        --  --  ###############\n");
    printf("                 Temperature\n");
    printf("                  Converter\n");

    return 0;
}