#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>

// surrealism meets CPU usage monitor
// let's create some imaginary variables

int electrostatic_frog = 0;
int interdimensional_cactus = 0;
int quantum_sardine = 0;
int cosmic_toaster = 0;

int main() {
    printf("Welcome to the surreal CPU usage monitor!\n");
    printf("Let's check some imaginary variables:\n");
    printf("Electrostatic frog: %d\n", electrostatic_frog);
    printf("Interdimensional cactus: %d\n", interdimensional_cactus);
    printf("Quantum sardine: %d\n", quantum_sardine);
    printf("Cosmic toaster: %d\n", cosmic_toaster);

    printf("\nChecking CPU usage...\n");

    // let's loop in a surreal way
    while (1) {
        electrostatic_frog++;
        if (electrostatic_frog % 50000 == 0) {
            printf("\nElectrostatic frog just jumped to %d\n", electrostatic_frog);
        }

        interdimensional_cactus += 2;
        if (interdimensional_cactus % 100000 == 0) {
            printf("Interdimensional cactus is now %d\n", interdimensional_cactus);
        }

        quantum_sardine += 5;
        if (quantum_sardine % 200000 == 0) {
            printf("Quantum sardine just multiplied to %d\n", quantum_sardine);
        }

        cosmic_toaster -= 3;
        if (cosmic_toaster % 250000 == 0) {
            printf("Cosmic toaster is now at %d\n", cosmic_toaster);
        }

        // sleep for a while to make it surreal
        usleep(50000);
    }

    return 0;
}