#include <stdio.h>

int main() {
  printf("Welcome to my scientific program! Let's have some fun with numbers!\n");

  int x = 5;
  float y = 2.5;
  double z = 3.14159;
  
  printf("x = %d, y = %f, z = %f\n", x, y, z);
  
  // Let's do some mathematical operations!
  printf("x + y = %f\n", x+y);
  printf("x - y = %f\n", x-y);
  printf("x * y = %f\n", x*y);
  printf("x / y = %f\n", x/y);

  printf("Now let's calculate some areas!\n");
  
  int length = 7;
  int width = 4;
  float circleRadius = 2.5;
  
  int rectangleArea = length * width;
  printf("The rectangle's area is: %d\n", rectangleArea);
  
  float circleArea = 3.14 * circleRadius * circleRadius;
  printf("The circle's area is: %f\n", circleArea);
  
  printf("Thanks for using my program! Have a great day!\n");
  
  return 0;
}