#include <stdio.h>

int main()
{
  int a = 5;
  int b = 9;
  int c;

  printf("Welcome to the cheerful Bitwise Operations example program!\n");
  printf("In this program, we will be using the &, |, ^, and ~ operators.\n");
  printf("Let’s start by setting a to 5 and b to 9.\n\n");

  printf("a = %d\n", a);
  printf("b = %d\n\n", b);

  printf("Now let’s apply some bitwise operations to these values.\n\n");

  c = a & b;
  printf("a & b = %d\n", c);

  c = a | b;
  printf("a | b = %d\n", c);

  c = a ^ b;
  printf("a ^ b = %d\n", c);

  c = ~a;
  printf("~a = %d\n", c);

  printf("\nWow! These operators sure are powerful!\n");
  printf("Thanks for joining me in this cheerful example program.\n");

  return 0;
}