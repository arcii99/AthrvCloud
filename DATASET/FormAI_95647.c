#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#define WIDTH 80
#define HEIGHT 30

int main(int argc, char**argv) {
    if (argc < 2) {
        printf("Usage: %s <text to convert to ASCII art>\n", argv[0]);
        return 1;
    }

    char* text = argv[1];
    char output[HEIGHT][WIDTH];

    // Set all characters in output to space
    memset(output, ' ', sizeof(output));

    // Convert each character in text to ASCII art
    int i, j, len = strlen(text);
    for (i = 0; i < len; i++) {
        char ch = toupper(text[i]);
        if (ch < 'A' || ch > 'Z') {
            continue;
        }
        int index = ch - 'A';
        const char* ascii_art =
        "                                                                                "
        "                                                                                "
        "                                                                                "
        "                                                                                "
        "                                             #######                            "
        "                                          ##########                            "
        "                                         ############                           "
        "                                ###     ##############      ###                  "
        "                       ############################ ###################           "
        "                  ################################### ##################         "
        "               ####################################### #################         "
        "             ########################################### ###############         "
        "            #############################################################         "
        "          ##############################%c#####################%c#############     "
        "        ###############################%c###################%c################   "
        "      ################################%c##################%c#################   "
        "     ##################################%c##################%c################   "
        "     ###################################%c################%c#################   "
        "    ####################################%c################%c#################   "
        "   #####################################%c################%c################   "
        "   ######################################%c##############%c#################   "
        "   #######################################%c############%c#################    "
        "  ########################################%c############%c#################    "
        "  ########################################%c############%c#################    "
        "  ########################################%c############%c#################    "
        "  ########################################%c############%c#################    "
        "  ########################################%c############%c#################    "
        "  ########################################%c############%c#################    "
        "  ########################################%c############%c#################    "
        "  ########################################%c############%c#################    "
        "   #######################################%c##########%c#################     "
        "   #######################################%c##########%c#################     "
        "   #######################################%c##########%c#################     "
        "    ######################################%c########%c#################      "
        "     #####################################%c######%c#################       "
        "      ###################################%c#####%c#################         "
        "        ###############################%c######%c################           "
        "          ############################%c#######%c##############             "
        "             ########################%c########%c############                "
        "                ####################%c##########%c########                   "
        "                     ##############%c############%c##                        "
        "                                                                                "
        "                                                                                "
        "                                                                                "
        "                                                                                "
        "                                                                                ";
        for (j = 0; j < 45; j++) {
            output[j][i*3] = ascii_art[j*79 + index*3];
            output[j][i*3+1] = ascii_art[j*79 + index*3 + 1];
            output[j][i*3+2] = ascii_art[j*79 + index*3 + 2];
        }
    }

    // Print the output
    for (i = 0; i < HEIGHT; i++) {
        for (j = 0; j < WIDTH; j++) {
            printf("%c", output[i][j]);
        }
        printf("\n");
    }

    return 0;
}