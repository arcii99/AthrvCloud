#include<stdio.h>
#define PI 3.14159 // let PI be the ratio of a circle's circumference to its diameter
int main()
{
    float radius, area;
    printf("Enter the radius of circle:");
    scanf("%f", &radius);
    area = PI * radius * radius;
    printf("The area of the circle is: %f", area);
    return 0;
}