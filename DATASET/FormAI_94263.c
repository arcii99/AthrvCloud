#include<stdio.h>

int main() {

  /* Initializing variables for arithmetic operations */
  int num1 = 10;
  int num2 = 5;

  /* Performing arithmetic operations */
  int sum = num1 + num2;
  int diff = num1 - num2;
  int prod = num1 * num2;
  int quot = num1 / num2;
  int mod = num1 % num2;

  printf("I am a program that can perform basic arithmetic operations in C programming language.\n\n");

  printf("Given two numbers num1 = %d and num2 = %d\n\n", num1, num2);

  printf("Sum of num1 and num2 is %d\n", sum);
  printf("Difference between num1 and num2 is %d\n", diff);
  printf("Product of num1 and num2 is %d\n", prod);
  printf("Quotient when num1 is divided by num2 is %d\n", quot);
  printf("Remainder when num1 is divided by num2 is %d\n", mod);

  return 0;
}