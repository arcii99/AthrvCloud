#include <stdio.h>

int main() {
  int a = 10, b = 20, c, d, e;

  // Add a and b and store the result in c
  c = a + b;
  printf("The sum of %d and %d is %d\n", a, b, c);

  // Subtract b from a and store the result in d
  d = a - b;
  printf("The difference between %d and %d is %d\n", a, b, d);

  // Multiply a and b and store the result in e
  e = a * b;
  printf("The product of %d and %d is %d\n", a, b, e);

  // Divide b by a and store the result in c
  c = b / a;
  printf("The division of %d by %d is %d\n", b, a, c);

  return 0;
}