#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int main() {

   // Display game instructions   
   printf("*****************************************\n");
   printf("Welcome to the C Table Game!\n");
   printf("The aim of the game is to guess a number\n");
   printf("that has been randomly generated by the computer.\n");
   printf("You have ten chances to guess the number correctly.\n");
   printf("Good luck and enjoy the game!\n");
   printf("*****************************************\n");

   // Set up game variables
   int number = 0;
   int guess = 0;
   int tries = 0;
   time_t t;
   
   // Initialize RNG
   srand((unsigned) time(&t));
   number = rand() % 100 + 1;

   // Loop until the player guesses the number correctly or runs out of tries
   while (tries < 10) {
      printf("Guess a number between 1 and 100: ");
      scanf("%d", &guess);
      tries++;
      
      // Validate input
      if (guess < 1 || guess > 100) {
         printf("Invalid guess. Please enter a number between 1 and 100.\n");
         continue;
      }
      
      // Check the guess against the random number
      if (guess < number) {
         printf("Too low! Guess higher.\n");
      } else if (guess > number) {
         printf("Too high! Guess lower.\n");
      } else {
         printf("Congratulations! You guessed the number in %d tries.\n", tries);
         break;
      }
   }

   // Player did not guess the number in 10 tries
   if (tries == 10) {
      printf("Sorry, you did not guess the number. It was %d.\n", number);
   }

   return 0;
}