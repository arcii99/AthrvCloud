#include<stdio.h>

int main()
{
    int x;                                      //Initialize an integer variable
    printf("Enter a number: ");                  //Print a message
    scanf("%d",&x);                              //Accept input from user
    if(x%2 == 0){                                //Check if the number is even
        printf("The number you entered is even"); //Print a message
    }
    else{                                        //If the number is odd
        printf("The number you entered is odd");  //Print a message
    }
    return 0;                                     //End of program
}