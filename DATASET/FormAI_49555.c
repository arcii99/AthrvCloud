#include <stdio.h>
#include <sys/socket.h>
#include <arpa/inet.h>

int main() {
    int sockfd = socket(AF_INET, SOCK_STREAM, 0); // creating a TCP socket
    if (sockfd == -1) {
        printf("Error occurred while creating a socket.\n");
        return 1; // exit program with error
    }

    // SMTP server details
    char server_address[] = "smtp.example.com";
    int server_port = 25;

    // creating a socket address structure for server
    struct sockaddr_in server;
    server.sin_family = AF_INET;
    server.sin_port = htons(server_port);
    inet_pton(AF_INET, server_address, &server.sin_addr);

    // connecting to SMTP server
    if (connect(sockfd, (struct sockaddr *)&server, sizeof(server)) != 0) {
        printf("Error occurred while connecting to server.\n");
        return 1; // exit program with error
    }

    // reading server welcome message
    char buffer[1024] = {0};
    int status = read(sockfd, buffer, 1024);
    if (status < 0) {
        printf("Error occurred while reading server message.\n");
        return 1; // exit program with error
    }
    printf("Server: %s", buffer);

    // sending HELO command to server
    char helo_command[] = "HELO example.com\r\n";
    send(sockfd, helo_command, strlen(helo_command), 0);

    // reading server response to HELO command
    status = read(sockfd, buffer, 1024);
    if (status < 0) {
        printf("Error occurred while reading server message.\n");
        return 1; // exit program with error
    }
    printf("Server: %s", buffer);

    // sending MAIL FROM command to server
    char mail_from_command[] = "MAIL FROM:<sender@example.com>\r\n";
    send(sockfd, mail_from_command, strlen(mail_from_command), 0);

    // reading server response to MAIL FROM command
    status = read(sockfd, buffer, 1024);
    if (status < 0) {
        printf("Error occurred while reading server message.\n");
        return 1; // exit program with error
    }
    printf("Server: %s", buffer);

    // sending RCPT TO command to server
    char rcpt_to_command[] = "RCPT TO:<recipient@example.com>\r\n";
    send(sockfd, rcpt_to_command, strlen(rcpt_to_command), 0);

    // reading server response to RCPT TO command
    status = read(sockfd, buffer, 1024);
    if (status < 0) {
        printf("Error occurred while reading server message.\n");
        return 1; // exit program with error
    }
    printf("Server: %s", buffer);

    // sending DATA command to server
    char data_command[] = "DATA\r\n";
    send(sockfd, data_command, strlen(data_command), 0);

    // reading server response to DATA command
    status = read(sockfd, buffer, 1024);
    if (status < 0) {
        printf("Error occurred while reading server message.\n");
        return 1; // exit program with error
    }
    printf("Server: %s", buffer);

    // sending email message to server
    char email_message[] = "From: sender@example.com\r\nTo: recipient@example.com\r\nSubject: SMTP Client Example\r\n\r\nThis is a test email generated by SMTP Client program.\r\n.\r\n";
    send(sockfd, email_message, strlen(email_message), 0);

    // reading server response to email message
    status = read(sockfd, buffer, 1024);
    if (status < 0) {
        printf("Error occurred while reading server message.\n");
        return 1; // exit program with error
    }
    printf("Server: %s", buffer);

    // sending QUIT command to server
    char quit_command[] = "QUIT\r\n";
    send(sockfd, quit_command, strlen(quit_command), 0);

    // reading server response to QUIT command
    status = read(sockfd, buffer, 1024);
    if (status < 0) {
        printf("Error occurred while reading server message.\n");
        return 1; // exit program with error
    }
    printf("Server: %s", buffer);

    // closing socket
    close(sockfd);

    return 0; // exit program without error
}