#include <stdio.h>

int main() {
    printf("Wooohooooo! I'm so excited to write a scientific program in C!!\n");

    float x, y, z;

    printf("Enter the value of x: ");
    scanf("%f", &x);

    printf("Enter the value of y: ");
    scanf("%f", &y);

    printf("Enter the value of z: ");
    scanf("%f", &z);

    float a = (3 * x) + (4 * y) - (5 * z);
    float b = (5 * x) - (6 * y) + (3 * z);
    float c = (2 * x) + (7 * y) + (4 * z);

    printf("The value of a is %.2f\n", a);
    printf("The value of b is %.2f\n", b);
    printf("The value of c is %.2f\n", c);

    printf("Wow! That was awesome!!\n");

    return 0;
}