void generate_ascii_art(char* text) {
    int len = strlen(text);

    // output top horizontal line
    for(int i = 0; i < len; i++) {
        printf(" ____ ");
    }
    printf("\n");

    // output first row of ASCII art for each character
    for(int i = 0; i < 7; i++) {
        for(int j = 0; j < len; j++) {
            int ascii_index = (int) text[j] - 32;
            printf("|%s| ", ascii_alphabet[ascii_index][i]);
        }
        printf("\n");
    }

    // output bottom horizontal line
    for(int i = 0; i < len; i++) {
        printf(" ¯¯¯¯ ");
    }
    printf("\n");
}

