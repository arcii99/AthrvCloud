#include <stdio.h>

int main()
{
    int a = 10, b = 5;
    float c = 5.5, d = 2.5;

    // Addition of two integers
    int addition_int = a + b;
    printf("Addition of two integers: %d\n", addition_int);

    // Subtraction of two integers
    int subtraction_int = a - b;
    printf("Subtraction of two integers: %d\n", subtraction_int);

    // Multiplication of two integers
    int multiplication_int = a * b;
    printf("Multiplication of two integers: %d\n", multiplication_int);

    // Division of two integers
    float division_int = (float)a / b;
    printf("Division of two integers: %.2f\n", division_int);

    // Addition of two floats
    float addition_float = c + d;
    printf("Addition of two floats: %.2f\n", addition_float);

    // Subtraction of two floats
    float subtraction_float = c - d;
    printf("Subtraction of two floats: %.2f\n", subtraction_float);

    // Multiplication of two floats
    float multiplication_float = c * d;
    printf("Multiplication of two floats: %.2f\n", multiplication_float);

    // Division of two floats
    float division_float = c / d;
    printf("Division of two floats: %.2f\n", division_float);

    // Modulo of two integers
    int modulo_int = a % b;
    printf("Modulo of two integers: %d\n", modulo_int);

    // Increment of an integer
    int increment_int = ++a;
    printf("Increment of an integer: %d\n", increment_int);

    // Decrement of an integer
    int decrement_int = --a;
    printf("Decrement of an integer: %d\n", decrement_int);

    return 0;
}