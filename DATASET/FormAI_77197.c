#include <stdio.h>

int main(void) {
  int a = 10;
  int b = 5;
  int c = 0;

  c = a + b;
  printf("%d + %d = %d\n", a, b, c);

  c = a - b;
  printf("%d - %d = %d\n", a, b, c);

  c = a * b;
  printf("%d * %d = %d\n", a, b, c);

  c = a / b;
  printf("%d / %d = %d\n", a, b, c);

  c = a % b;
  printf("%d %% %d = %d\n", a, b, c);

  return 0;
}