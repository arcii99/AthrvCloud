// Function to classify the image based on its pixel values
void imageClassification(int image[28][28]) {
    int totalPixels = 28 * 28;
    int whitePixels = 0, blackPixels = 0;
    for (int i = 0; i < 28; i++) {
        for (int j = 0; j < 28; j++) {
            if (image[i][j] == 0) {
                whitePixels++;
            } else {
                blackPixels++;
            }
        } 
    }

    // Determine the percentage of black pixels in the image
    float blackPercentage = ((float)blackPixels / (float)totalPixels) * 100;

    // Classify the image based on its pixel values
    if (blackPercentage < 10) {
        printf("The image is a white picture.\n");
    } else if (blackPercentage > 90) {
        printf("The image is a black picture.\n");
    } else if (blackPercentage > 50) {
        printf("The image can be classified as a grey picture.\n");
    } else {
        printf("The image can be classified as a colored picture.\n");
    }
}

