#include<stdio.h>

int main() {
    int a = 10;
    int b = 20;
    int c;

    // Addition
    c = a + b;
    printf("Sum of %d and %d is %d \n", a, b, c);

    // Subtraction
    c = a - b;
    printf("Difference of %d and %d is %d \n", a, b, c);

    // Multiplication
    c = a * b;
    printf("Product of %d and %d is %d \n", a, b, c);

    // Division
    c = b / a;
    printf("%d divided by %d is %d \n", b, a, c);

    // Modulus Operation
    c = b % a;
    printf("Modulus of %d and %d is %d \n", b, a, c);

    // Increment Operation
    a++;
    b++;
    printf("After incrementing, a is %d and b is %d \n", a, b);

    // Decrement Operation
    a--;
    b--;
    printf("After decrementing, a is %d and b is %d \n", a, b);

    return 0;
}