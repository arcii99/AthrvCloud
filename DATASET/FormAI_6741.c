#include <stdio.h> // This header file is important for input-output operations

int main() // The main function is the entry point of the program
{
    int i; // We will use this integer variable to store the random number generated by the program

    printf("\n\nRandom numbers generated will range from 0 to 99.\n\n"); // A welcome message is always nice!

    srand(time(NULL)); // This function is important as it seeds the random number generator with the current time, so that different random numbers are produced each time the program is executed

    for(i = 0; i < 10; i++) // A loop is used to generate 10 random numbers
    {
        printf("\n%d. %d", i+1, rand() % 100); // The rand() function generates the random number and the modulus % operator limits its range to 0-99
    }

    printf("\n\n"); // For readability, add some empty lines at the end of the program

    return 0; // The program ends here
}