#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include <math.h>

int main() {
  int num1, num2;
  float ans, userAns;
  char operator;
  
  srand(time(0)); // to generate random numbers
  
  printf("Welcome to the C Math Exercise Program!\n\n");
  printf("You will be asked to answer math questions randomly generated by the program.\n");
  printf("Please type your answer and press enter. To exit the program, enter -999.\n\n");
  
  while(1) {
    num1 = rand() % 101; // first random number between 0 and 100
    num2 = rand() % 101; // second random number between 0 and 100
    
    switch(rand() % 4) { // randomly select one of the four operators
      case 0:
        operator = '+';
        ans = num1 + num2;
        break;
      case 1:
        operator = '-';
        ans = num1 - num2;
        break;
      case 2:
        operator = '*';
        ans = num1 * num2;
        break;
      case 3:
        operator = '/';
        ans = (float) num1 / num2; // cast to float to get decimal answers for division
        ans = round(ans * 100.0) / 100.0; // round the answer to two decimal places
        break;
    }
    
    printf("What is %d %c %d?\n", num1, operator, num2);
    scanf("%f", &userAns);
    
    if(userAns == -999) { // if user wants to exit program
      printf("\nThank you for using the C Math Exercise Program!\n");
      break;
    }
    
    if(ans == userAns) { // if answer is correct
      printf("Congratulations! Your answer is correct.\n\n");
    }
    else { // if answer is incorrect
      printf("Sorry, your answer is incorrect. The correct answer is %.2f.\n\n", ans);
    }
  }
  
  return 0;
}