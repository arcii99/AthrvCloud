#include <stdio.h>

int main() {
  float radius, area;
  printf("My love, could you enter the radius of your beautiful smile? ");
  scanf("%f", &radius);
  area = 3.14159 * radius * radius;
  printf("Thank you, my darling. The area of your lovely smile is %f.\n", area);
  return 0;
}