/**
 * Perform the key schedule over the given key to calculate the round keys
 */
void key_schedule(uint8_t *key) {
    uint8_t temp[BLOCK_SIZE];
    int i, j, k;

    // Copy the first 16 bytes of the key to the round key for round 0
    memcpy(round_keys[0], key, BLOCK_SIZE);

    // Iterate over the remaining rounds to derive subsequent round keys
    for (i = 1; i <= NUM_ROUNDS; i++) {
        // Rotate the bytes of the previous round key by 4 bytes to the left
        memcpy(temp, round_keys[i-1]+4, BLOCK_SIZE-4);
        memcpy(temp+(BLOCK_SIZE-4), round_keys[i-1], 4);
        // Apply the S-box transformation to each byte of the rotated bytes
        for (j = 0; j < BLOCK_SIZE; j++) {
            temp[j] = SBOX[temp[j]];
        }
        // XOR the first 4 bytes of the previous round key with a round constant
        temp[0] ^= (1 << (i-1));
        // Finally, set the round key for this round by XORing the previous round key with the transformed bytes
        for (k = 0; k < BLOCK_SIZE; k++) {
            round_keys[i][k] = round_keys[i-1][k] ^ temp[k];
        }
    }
}

/**
 * Perform a single round of the encryption algorithm on the given block
 */
void cipher_round(uint8_t *block, uint8_t *round_key) {
    uint8_t temp[BLOCK_SIZE];
    int i, j;

    // Copy the block to a temporary array
    memcpy(temp, block, BLOCK_SIZE);

    // Substitute each byte of the block using the S-box
    for (i = 0; i < BLOCK_SIZE; i++) {
        temp[i] = SBOX[temp[i]];
    }

    // Perform the linear transformation on the substitution result
    for (i = 0; i < BLOCK_SIZE; i++) {
        block[i] = 0;
        for (j = 0; j < BLOCK_SIZE; j++) {
            block[i] ^= temp[j] & round_key[(i+j) % BLOCK_SIZE];
        }
    }

    // XOR the modified block with the original block to form the new block
    for (i = 0; i < BLOCK_SIZE; i++) {
        block[i] ^= temp[i];
    }
}

/**
 * Perform the actual encryption of the given plaintext using the given key
 */
void encrypt(uint8_t *plaintext, uint8_t *key, uint8_t *ciphertext) {
    int i;

    // Derive the round keys from the given key
    key_schedule(key);

    // Copy the plaintext to the ciphertext
    memcpy(ciphertext, plaintext, BLOCK_SIZE);

    // Perform the prescribed number of rounds on the ciphertext with the derived round keys
    for (i = 0; i < NUM_ROUNDS; i++) {
        cipher_round(ciphertext, round_keys[i+1]);
    }
}

