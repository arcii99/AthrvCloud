#include <stdio.h>

// Function to initialize the drone remote control
void init_drone_remote() {
    printf("Drone remote control initialized.\n");
}

// Function to connect the drone to the remote control
void connect_drone() {
    printf("Drone connected to remote control.\n");
}

// Function to fly the drone
void fly_drone() {
    printf("Drone is flying.\n");
}

// Function to land the drone
void land_drone() {
    printf("Drone is landing.\n");
}

// Function to disconnect the drone
void disconnect_drone() {
    printf("Drone disconnected from remote control.\n");
}

int main() {
    // Initialize the drone remote control
    init_drone_remote();

    // Connect the drone to the remote control
    connect_drone();

    // Fly the drone
    fly_drone();

    // Land the drone
    land_drone();

    // Disconnect the drone
    disconnect_drone();

    return 0;
}