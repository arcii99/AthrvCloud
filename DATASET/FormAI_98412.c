#include<stdio.h>

int main(){

    int num1 = 30; //declaring a random variable num1 with a value 30
    int num2 = 15; //declaring a random variable num2 with a value 15

    printf("Hey programmer, I am Bitwise Billy! Let's have some fun with Bitwise operations!\n");

    printf("The value of num1 is: %d\n", num1); //printing the value of num1
    printf("The value of num2 is: %d\n", num2); //printing the value of num2
    
    printf("\nAlright, let's perform some Bitwise AND operations!\n");

    int result1 = num1 & num2; //performing Bitwise AND on num1 and num2, storing the result in result1
    printf("The result of num1 & num2 is: %d\n", result1); //printing the result of Bitwise AND

    printf("\nNow, let's perform some Bitwise OR operations!\n");

    int result2 = num1 | num2; //performing Bitwise OR on num1 and num2, storing the result in result2
    printf("The result of num1 | num2 is: %d\n", result2); //printing the result of Bitwise OR

    printf("\nOh, wait! Let's perform Bitwise XOR operations!\n");

    int result3 = num1 ^ num2; //performing Bitwise XOR on num1 and num2, storing the result in result3
    printf("The result of num1 ^ num2 is: %d\n", result3); //printing the result of Bitwise XOR

    printf("\nPhew! That was some crazy Bitwise fun! I hope you enjoyed hanging out with me, Bitwise Billy!\n");

    return 0; //exiting the program with zero errors
}