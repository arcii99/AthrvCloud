#include<stdio.h>
#include<stdlib.h>
#include<sys/socket.h>
#include<netinet/in.h>
#include<arpa/inet.h>
#include<unistd.h>
#include<string.h>

int main(){

    int server_socket, new_socket;
    struct sockaddr_in server_address, client_address;
    char response_message[1024];

    //Creating socket
    server_socket = socket(AF_INET, SOCK_STREAM, 0);

    //Setting up server address
    server_address.sin_family = AF_INET;
    server_address.sin_port = htons(8080);
    server_address.sin_addr.s_addr = htonl(INADDR_ANY);

    //Binding address
    bind(server_socket, (struct sockaddr *)&server_address, sizeof(server_address));

    //Start listening
    listen(server_socket, 5); //5 is the backlog limit

    printf("Server is listening on port 8080...\n");

    while(1){

        //Accept the connection
        int client_address_len = sizeof(client_address);
        new_socket = accept(server_socket, (struct sockaddr *)&client_address, &client_address_len);

        //Read the request message
        char request_message[1024];
        read(new_socket, request_message, 1024);

        printf("Request message: %s\n", request_message);

        //Send the response message
        sprintf(response_message, "HTTP/1.1 200 OK\r\nContent-Type: text/html\r\n\r\n<!DOCTYPE html>\n<html>\n<head>\n<title>Simple Web Server</title>\n</head>\n<body>\n<h1>Welcome to Simple Web Server in Retro Style</h1>\n<p>This is a sample web page generated by the Simple Web Server in Retro Style.</p>\n</body>\n</html>\n");
        write(new_socket, response_message, strlen(response_message));

        //Close the connection
        close(new_socket);
    }

    return 0;
}