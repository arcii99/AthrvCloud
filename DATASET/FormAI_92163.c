#include <stdio.h>

int main() {
  printf("Hello, I am a Bitwise operations example program!\n");

  int a = 69;
  int b = 420;

  // Bitwise AND operation
  int resultAnd = a & b;
  printf("Result of bitwise AND operation: %d\n", resultAnd);

  // Bitwise OR operation
  int resultOr = a | b;
  printf("Result of bitwise OR operation: %d\n", resultOr);

  // Bitwise XOR operation
  int resultXor = a ^ b;
  printf("Result of bitwise XOR operation: %d\n", resultXor);

  // Bitwise NOT operation
  int resultNot = ~a;
  printf("Result of bitwise NOT operation: %d\n", resultNot);
  
  // Bitwise left shift operation
  int resultLeftShift = a << 2;
  printf("Result of bitwise left shift operation: %d\n", resultLeftShift);

  // Bitwise right shift operation
  int resultRightShift = b >> 3;
  printf("Result of bitwise right shift operation: %d\n", resultRightShift);

  return 0;
}