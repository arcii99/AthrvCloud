#include <stdio.h>
#include <stdlib.h>

int main() {

    float radius, area;
    printf("Please enter the radius of the circle: ");
    scanf("%f", &radius);

    // Geometric algorithm for area computation
    area = 3.14159265 * radius * radius;

    printf("The area of the circle is %f\n", area);
    return 0;
}