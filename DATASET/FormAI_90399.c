#include <stdio.h>

int main() {
    float celcius, fahrenheit;

    printf("My love, let me convert the temperature of your heart from Celsius to Fahrenheit - just enter the value:\n");
    scanf("%f", &celcius);

    fahrenheit = (celcius * 9 / 5) + 32;

    printf("My dear, your heart is so warm - it's %0.2f degrees Fahrenheit.\n", fahrenheit);

    return 0;
}