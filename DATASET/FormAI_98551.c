void generate_ascii_art(char* str)
{
    int str_length = strlen(str);
    int i, j;
    for (i = 0; i < ART_SIZE; i++) {
        for (j = 0; j < str_length; j++) {
            if (str[j] == ' ') {
                printf(" ");
            } else {
                printf("%c", ART[i][str[j] - 'A']);
            }
        }
        printf("\n");
    }
}

