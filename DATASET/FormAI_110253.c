#include <stdio.h>
int main()
{
    int x = 20;
    int y = 10;
    printf("Welcome to the math circus!\n");
    printf("Today, we have the amazing arithmetic program for you!\n");

    printf("Let's do some addition!\n");
    int sum = x + y;
    printf("%d + %d = %d\n", x, y, sum);

    printf("Now, let's do some subtraction... wait where did my clown nose go?\n");
    int difference = x - y;
    printf("%d - %d = %d\n", x, y, difference);

    printf("Next up, multiplication! This one's a bit tricky, so hold onto your hats!\n");
    int product = x * y;
    printf("%d x %d = %d\n", x, y, product);

    printf("Division time! Remember to share your answers with your friends!\n");
    int quotient = x / y;
    printf("%d / %d = %d\n", x, y, quotient);

    printf("Modulo time! This one will leave you flipping and jumping around like a monkey!\n");
    int remainder = x % y;
    printf("%d %% %d = %d\n", x, y, remainder);

    printf("Thank you for visiting the math circus! If you enjoyed our arithmetic show, please come back again soon!\n");

    return 0;
}