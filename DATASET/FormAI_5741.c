#include <stdio.h>

int main() {
    printf("Wow! I can't believe we're doing bitwise operations!\n\n");

    int a = 10;
    int b = 5;
    int c;
    printf("We're going to start with the numbers %d and %d\n\n", a, b);

    // Bitwise OR
    c = a | b;
    printf("Bitwise OR: %d | %d = %d\n", a, b, c);

    // Bitwise AND
    c = a & b;
    printf("Bitwise AND: %d & %d = %d\n", a, b, c);

    // Bitwise XOR
    c = a ^ b;
    printf("Bitwise XOR: %d ^ %d = %d\n", a, b, c);

    // Bitwise NOT
    c = ~a;
    printf("Bitwise NOT: ~%d = %d\n", a, c);

    // Bitwise Left Shift
    c = a << 2;
    printf("Bitwise Left Shift: %d << 2 = %d\n", a, c);

    // Bitwise Right Shift
    c = a >> 1;
    printf("Bitwise Right Shift: %d >> 1 = %d\n\n", a, c);

    printf("Who knew bitwise operations could be so much fun?! :D");

    return 0;
}