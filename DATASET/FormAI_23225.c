#include <stdio.h>

int main() {
    printf("Hello there! I'm here to teach you about Bitwise operations in C.\n");
    printf("Let's start with the AND (&) operator.\n");
    int a = 6;
    int b = 3;
    int c = a & b;
    printf("a & b = %d\n", c);
    
    printf("Now let's move on to the OR (|) operator.\n");
    int d = 6;
    int e = 3;
    int f = d | e;
    printf("d | e = %d\n", f);
    
    printf("Next up is the XOR (^) operator.\n");
    int g = 6;
    int h = 3;
    int i = g ^ h;
    printf("g ^ h = %d\n", i);
    
    printf("Let's try out the left shift (<<) operator.\n");
    int j = 6;
    int k = j << 1;
    printf("j << 1 = %d\n", k);
    
    printf("Now we'll move on to the right shift (>>) operator.\n");
    int l = 6;
    int m = l >> 1;
    printf("l >> 1 = %d\n", m);
    
    printf("Finally, let's see how we can use the bitwise NOT (~) operator.\n");
    int n = 6;
    int o = ~n;
    printf("~n = %d\n", o);
    
    printf("I hope this example program has been helpful in showing you how to use Bitwise operations in C. Happy coding!\n");
    return 0;
}