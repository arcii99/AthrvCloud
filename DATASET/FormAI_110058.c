#include <stdio.h>

int main(void) {

    int x = 420;
    int y = 69;
    int z = x & y;  // bitwise AND operator
    
    printf("I am a cool Bitwise Operator Code. Let me show you some magic...\n\n");

    printf("X: %d\n", x);
    printf("Y: %d\n", y);
    printf("Z: %d\n", z);
    printf("\n");
    
    printf("First, we'll test out the bitwise AND operator:\n");
    printf("X & Y: %d\n", z);
    printf("Using two numbers that everyone likes, X: 420 and Y: 69\n");
    printf("\n");
  
    int a = 420;
    int b = 69;
    int c = a | b;  // bitwise OR operator
    
    printf("Next up, the bitwise OR operator:\n");
    printf("A: %d\n", a);
    printf("B: %d\n", b);
    printf("C: %d\n", c);
    printf("Using two numbers that everyone likes, A: 420 and B: 69\n");
    printf("\n");

    int d = 420;
    int e = ~d;  // bitwise NOT operator
    
    printf("Now the bitwise NOT operator:\n");
    printf("D: %d\n", d);
    printf("E: %d\n", e);
    printf("Because 420 isn't enough for everyone in the world\n");
    printf("\n");

    int f = 69;
    int g = f << 2;  // left shift operator
    
    printf("Now the bitwise left shift operator:\n");
    printf("F: %d\n", f);
    printf("G: %d\n", g);
    printf("Using the number everyone associates with the position ;)\n");
    printf("\n");

    int h = 420;
    int i = h >> 2;  // right shift operator
    
    printf("Last up, the bitwise right shift operator:\n");
    printf("H: %d\n" ,h);
    printf("I: %d\n", i);
    printf("Using the first two numbers of everyone's favorite number...\n");
    printf("\n");
    
    return 0;
}