#include <stdio.h>

// Romeo and Juliet-style program to find the area of a rectangle

int main() {
    int length, width, area;
    printf("O Romeo, O Romeo, what is the length of this fair rectangle?\n");
    scanf("%d", &length);
    printf("My fair Juliet, the width of this rectangle I know to be what?\n");
    scanf("%d", &width);
    area = length * width;
    printf("O Romeo, the area of this fair rectangle is %d! How can this be?\n", area);
    printf("My fair Juliet, it is simple geometry! The area of a rectangle is found by multiplying its length and width.\n");
    printf("O Romeo, this is most wondrous! I am amazed by this mathematical marvel!\n");
    return 0;
}