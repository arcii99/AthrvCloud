#include <stdio.h>

int main() {

  printf("Wow, I'm so surprised by how easy this was to write!\n");

  // Let's calculate the area of a circle
  float radius = 5.0;
  float pi = 3.14;
  float area = pi * radius * radius;

  // Print the results
  printf("The area of a circle with radius %.2f is %.2f\n", radius, area);

  // Let's also calculate the perimeter of a square
  float side = 10;
  float perimeter = 4 * side;

  // Print the results
  printf("The perimeter of a square with side %.2f is %.2f\n", side, perimeter);

  // Lastly, let's calculate the volume of a sphere
  float sphere_radius = 7.5;
  float sphere_volume = (4.0/3.0) * pi * sphere_radius * sphere_radius * sphere_radius;

  // Print the results
  printf("The volume of a sphere with radius %.2f is %.2f\n", sphere_radius, sphere_volume);

  return 0;
}