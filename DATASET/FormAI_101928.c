#include<stdio.h>

int main()
{
    int salary;
    int expenses;
    int saving;
    int reality;
    int illusion;

    printf("Welcome to the surreal world of personal finance planning!\n");

    printf("Please enter your monthly salary: ");
    scanf("%d", &salary);

    printf("Please enter your monthly expenses: ");
    scanf("%d", &expenses);

    printf("Please wait while we process the data...\n");

    saving = salary - expenses;

    printf("Congratulations! You have saved %d dollars this month.\n", saving);

    printf("But wait...is this the reality or just an illusion?\n");

    printf("Let's perform a reality check...\n");

    reality = saving / 2;

    printf("Uh oh, it seems like the reality is different from what you thought it was.\n");

    printf("Your real savings is only %d dollars this month.\n", reality);

    printf("But don't worry, we have a solution for you!\n");

    illusion = saving * 2;

    printf("You can now live in an illusion and feel like you have saved %d dollars this month!\n", illusion);

    printf("Thank you for using our surreal personal finance planner. Good luck!\n");

    return 0;
}