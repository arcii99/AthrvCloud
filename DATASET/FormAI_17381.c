#include <stdio.h>

int main() {
    int num1, num2, sum;

    printf("Welcome to the Happy Adder program!\n");
    printf("Enter the first number: ");
    scanf("%d", &num1);
    printf("Enter the second number: ");
    scanf("%d", &num2);

    // Adding two numbers
    sum = num1 + num2;

    printf("\nWohooo! Adding these numbers makes you happy! The result is: %d\n\n", sum);

    return 0;
}