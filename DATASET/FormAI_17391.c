#include <stdio.h>

int main() {
  int a = 10;
  int b = 3;
  
  printf("a = %d\n", a);
  printf("b = %d\n", b);

  printf("\n");

  printf("a * b = %d\n", a * b);
  printf("a / b = %d\n", a / b);
  printf("a %% b = %d\n", a % b);

  printf("\n");

  printf("a + b = %d\n", a + b);
  printf("a - b = %d\n", a - b);
  printf("++a = %d\n", ++a);
  printf("--b = %d\n", --b);

  printf("\n");

  printf("a += 4 = %d\n", a += 4);
  printf("b -= 2 = %d\n", b -= 2);

  printf("\n");

  printf("-a = %d\n", -a);
  printf("+b = %d\n", +b);

  return 0;
}