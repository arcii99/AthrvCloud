#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int main() {
    printf("Welcome to the C Data Mining program!\n");
    printf("Please enter a number between 1-1000: ");
    int num;
    scanf("%d", &num);
    srand(time(0));
    int rand_num = rand() % 1000;
    printf("The random number generated is: %d\n", rand_num);
    int diff = abs(num - rand_num);
    if (diff <= 100) {
        printf("The difference between your number and the random number is within 100. You are getting close!\n");
    }
    else if (diff <= 500) {
        printf("The difference between your number and the random number is within 500. You are on the right track!\n");
    }
    else {
        printf("The difference between your number and the random number is more than 500. Keep trying!\n");
    }
    printf("Now let's use data mining to determine if your favorite color matches the randomly generated color.\n");
    char fav_color[50];
    printf("Please enter your favorite color: ");
    scanf("%s", fav_color);
    char rand_color[50];
    char* colors[10] = {"red", "blue", "green", "yellow", "orange", "purple", "brown", "white", "black", "gray"};
    int rand_index = rand() % 10;
    strcpy(rand_color, colors[rand_index]);
    printf("The random color generated is: %s\n", rand_color);
    int match = strcmp(fav_color, rand_color);
    if (match == 0) {
        printf("Congratulations! Your favorite color matches the random color generated by our data mining algorithm!\n");
    }
    else {
        printf("Unfortunately, your favorite color does not match the random color generated by our data mining algorithm. Keep trying!\n");
    }
    return 0;
}