#include <stdio.h>

int main() {
    printf("Let's solve a Math problem together! Yay!\n");

    int a = 50;
    int b = 25;

    printf("\nWe have been given two numbers: %d and %d\n\n", a, b);

    printf("Now, let's find their sum!\n");

    int sum = a + b;

    printf("\nThe sum of %d and %d is: %d\n", a, b, sum);

    printf("\nGreat job! Now, let's move on to finding the difference.\n");

    int difference = a - b;

    printf("\nThe difference of %d and %d is: %d\n", a, b, difference);

    printf("\nWow, you are doing amazing so far! Let's find the product of the two numbers now.\n");

    int product = a * b;

    printf("\nThe product of %d and %d is: %d\n", a, b, product);

    printf("\nLast but not least, let's find the quotient!\n");

    float quotient = (float) a / b;

    printf("\nThe quotient of %d and %d is: %.2f\n", a, b, quotient);

    printf("\nCongratulations on solving the Math problem! You rock!\n");

    return 0;
}