#include <stdio.h>
#include <stdlib.h>
#include <time.h>

void log_event(const char *event_type, const char *description) {
    FILE *log_file = fopen("system_log.txt", "a");
    if (log_file == NULL) {
        perror("Could not open file");
        return;
    }
    
    time_t current_time = time(NULL);
    char *time_string = ctime(&current_time);
    time_string[strlen(time_string)-1] = '\0';
    
    fprintf(log_file, "[%s] %s: %s\n", time_string, event_type, description);
    fclose(log_file);
}

int main() {
    printf("Welcome to the System Event Logger!\n\n");
    int option;
    do {
        printf("Select an option:\n");
        printf("1. Log an error\n");
        printf("2. Log a warning\n");
        printf("3. Log an information message\n");
        printf("4. Exit\n");
        printf(">> ");
        
        scanf("%d", &option);
        while (getchar() != '\n');
        
        switch(option) {
            case 1:
                log_event("ERROR", "An error occurred in the system");
                break;
            case 2:
                log_event("WARNING", "A warning was issued by the system");
                break;
            case 3:
                log_event("INFO", "An informational message was generated by the system");
                break;
            case 4:
                printf("Exiting...\n");
                break;
            default:
                printf("Invalid option. Please try again.\n");
                break;
        }
    } while (option != 4);
    
    return 0;
}