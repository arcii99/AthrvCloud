/* Program to add and subtract two numbers */
#include <stdio.h>

int main() {
    int num1, num2, sum, diff;

    printf("Let's spread some peaceful vibes by adding and subtracting two numbers!\n");

    printf("Enter the first number: ");
    scanf("%d", &num1);

    printf("Enter the second number: ");
    scanf("%d", &num2);

    sum = num1 + num2;
    diff = num1 - num2;

    printf("The sum of %d and %d is %d\n", num1, num2, sum);
    printf("The difference of %d and %d is %d\n", num1, num2, diff);

    printf("Sending positive energy your way, hope this code helped you relax. Peace out!\n");

    return 0;
}