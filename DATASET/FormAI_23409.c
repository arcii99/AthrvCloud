#include <stdio.h>

int main() {
    // Initialize our variables
    int num1 = 8;
    int num2 = 12;
    
    // Perform AND operation
    int result1 = num1 & num2;
    printf("The result of our AND operation is: %d\n", result1);
    
    // Perform OR operation
    int result2 = num1 | num2;
    printf("The result of our OR operation is: %d\n", result2);
    
    // Perform XOR operation
    int result3 = num1 ^ num2;
    printf("The result of our XOR operation is: %d\n", result3);
    
    // Perform LEFT SHIFT operation
    int result4 = num1 << 2;
    printf("The result of our LEFT SHIFT operation is: %d\n", result4);
    
    // Perform RIGHT SHIFT operation
    int result5 = num2 >> 2;
    printf("The result of our RIGHT SHIFT operation is: %d\n", result5);
    
    // Display a romantic message
    printf("You and I are like bits, we complement each other perfectly.\n");
    
    return 0;
}