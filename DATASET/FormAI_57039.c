#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int main(void) {
    int score = 0;      // Initialize score
    srand(time(0));     // Set seed for random number generator
    
    printf("Welcome to Terminal Game\n\n");
    printf("Instructions:\n");
    printf("- You have 10 turns to score as many points as possible\n");
    printf("- Guess a number between 1 and 10\n");
    printf("- If your guess matches the number generated by the computer, you get a point\n");
    printf("- If your guess is off by 1, you get half a point\n");
    printf("- If your guess is off by more than 1, you get 0 points\n\n");
    
    // Loop for 10 turns
    for (int i = 1; i <= 10; i++) {
        printf("Turn %d:\n", i);
        int guess;
        
        // Get user input
        printf("Enter your guess (1-10): ");
        scanf("%d", &guess);
        while (guess < 1 || guess > 10) {
            printf("Invalid input, try again: ");
            scanf("%d", &guess);
        }
        
        // Generate random number
        int number = rand() % 10 + 1;
        printf("The correct number was %d.\n", number);
        
        // Check if guess matches or is off by 1
        if (guess == number) {
            printf("Congratulations, you scored 1 point!\n");
            score++;
        } else if (abs(guess - number) == 1) {
            printf("Close, you scored 0.5 points!\n");
            score += 0.5;
        } else {
            printf("Sorry, you scored 0 points.\n");
        }
        
        printf("Your current score is %d.\n\n", score);
    }
    
    // Game over
    printf("Game over! Your final score is %d.\n", score);
    
    return 0;
}