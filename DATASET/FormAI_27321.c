#include <stdio.h>

int main()
{
    int a = 10;
    int b = 5;

    printf("Ahoy thar ye maties, let us perform some arithmetic!\n\n");

    int sum = a + b;
    printf("Ye scallywags! The sum o' %d and %d be %d!\n", a, b, sum);

    int difference = a - b;
    printf("Arr, me hearties! The difference betwixt %d and %d be %d!\n", a, b, difference);

    int product = a * b;
    printf("Blow me down! The product o' %d and %d be %d!\n", a, b, product);

    int quotient = a / b;
    printf("Shiver me timbers! The quotient o' %d and %d be %d!\n", a, b, quotient);

    int remainder = a % b;
    printf("Avast ye! The remainder when %d be divided by %d be %d!\n", a, b, remainder);

    return 0;
}