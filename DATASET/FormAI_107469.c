#include <stdio.h>
#include <stdlib.h>

int main() {
  int surreal = 7;
  int dream = 9;
  int reality = surreal + dream;
  if (reality == surreal * dream) {
    printf("The impossible has become possible!\n");
  } else {
    printf("Reality is just a figment of our imaginations...\n");
  }
  
  int insanity = 4;
  int madness = 2;
  int chaos = insanity - madness;
  if (chaos < 0) {
    printf("There is no order in this world...\n");
  } else {
    printf("The madness is just beginning!\n");
  }
  
  int illusion = 3;
  int delusion = 6;
  int hallucination = illusion / delusion;
  if (hallucination == 0) {
    printf("Reality is just a mirage...\n");
  } else {
    printf("The mind can create its own reality!\n");
  }
  
  int paradox = 2;
  int irony = 2;
  int confusion = paradox * irony;
  if (confusion == surreal + dream + insanity + madness + illusion + delusion) {
    printf("The universe is full of contradictions...\n");
  } else {
    printf("The world makes perfect sense to those who are insane.\n");
  }
  
  int enigma = 5;
  int mystery = 7;
  int conundrum = enigma % mystery;
  if (conundrum != 0) {
    printf("There are secrets hidden everywhere...\n");
  } else {
    printf("The answers may be obscure, but they are there!\n");
  }
  
  return 0;
}