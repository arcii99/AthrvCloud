#include <stdio.h>
#include <math.h>

int main()
{
    float radius, area;
    
    printf("Hello! Let's calculate the area of a circle!\n");
    printf("Please enter the radius: ");
    scanf("%f", &radius);
    
    area = M_PI * pow(radius, 2);
    
    printf("The area of the circle is: %f\n", area);
    printf("Yay! We did it! Keep coding and having fun!\n");
    
    return 0;
}