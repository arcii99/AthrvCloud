#include <stdio.h>

int main() {
    int base, height;

    printf("Greetings, noble user!\n");
    printf("I am your trusty triangle area calculator. Please enter the base and height of your triangle in medieval units: ");

    scanf("%d %d", &base, &height);

    int area = (base * height) / 2;

    printf("\nThank you, noble user! Your triangle has a majestic area of %d square medieval units.", area);

    return 0;
}