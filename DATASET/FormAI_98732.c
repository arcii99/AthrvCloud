#include <stdio.h>

int main() {
    float radius, area;
    printf("Enter radius of circle: ");
    scanf("%f", &radius);

    area = 3.14159 * radius * radius;

    printf("Area of circle with radius %f is %f\n", radius, area);

    return 0;
}