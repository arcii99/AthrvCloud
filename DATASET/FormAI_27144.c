#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <ctype.h>
#include <math.h>
#include <stdbool.h>
#include <unistd.h>

// Define constant values
#define MAX_WIDTH 50
#define MAX_HEIGHT 50

// Define the function to convert characters into ASCII art
void charToArt(char c) {
    // Define the ASCII art based on the character input
    switch(c) {
        case 'A':
        case 'a':
            printf("##   ##\n");
            printf("##   ##\n");
            printf("#######\n");
            printf("##   ##\n");
            printf("##   ##\n");
            break;
        case 'B':
        case 'b':
            printf("###### \n");
            printf("##    ##\n");
            printf("###### \n");
            printf("##    ##\n");
            printf("###### \n");
            break;
        case 'C':
        case 'c':
            printf(" ##### \n");
            printf("##    ##\n");
            printf("##     \n");
            printf("##    ##\n");
            printf(" ##### \n");
            break;
        // Add more cases for each character in the alphabet, numbers, and symbols
        // ...
        default:
            printf("        \n");
            printf("        \n");
            printf("        \n");
            printf("        \n");
            printf("        \n");
            break;
    }
}

int main() {
    // Initialize variables
    char message[MAX_WIDTH * MAX_HEIGHT];
    int messageLen, rows, cols, i, j, k;

    // Get input message
    printf("Enter message to convert to ASCII art:\n");
    fgets(message, sizeof(message), stdin);
    messageLen = strlen(message);

    // Remove newline character from input message
    if (message[messageLen - 1] == '\n') {
        message[messageLen - 1] = '\0';
        messageLen--;
    }

    // Calculate number of rows and columns needed for output ASCII art
    rows = ceil((double) messageLen / MAX_WIDTH);
    cols = MAX_WIDTH;

    // Initialize 2D array to store the ASCII art characters
    char art[MAX_HEIGHT][MAX_WIDTH * rows];

    // Loop through each character in the input message and save the corresponding ASCII art character in the 2D array
    for (i = 0; i < rows; i++) {
        for (j = 0; j < cols; j++) {
            k = i * cols + j;
            if (k < messageLen) {
                charToArt(message[k]);
            } else {
                charToArt(' ');
            }

            // Save the ASCII art character in the 2D array
            int rowOffset = (MAX_HEIGHT - 5) / 2; // Calculate vertical offset to center the ASCII art character
            int colOffset = (MAX_WIDTH - 9) / 2; // Calculate horizontal offset to center the ASCII art character
            int rowIdx = i * (MAX_HEIGHT - 1) + rowOffset;
            int colIdx = j * (MAX_WIDTH - 1) + colOffset;
            for (int m = 0; m < 5; m++) {
                for (int n = 0; n < 9; n++) {
                    art[rowIdx + m][colIdx + n] = getchar();
                }
                getchar(); // Consume newline character generated by charToArt function
            }
        }
    }

    // Print the ASCII art
    printf("\n");
    for (i = 0; i < MAX_HEIGHT * rows; i++) {
        for (j = 0; j < MAX_WIDTH * cols; j++) {
            printf("%c", art[i][j]);
        }
        printf("\n");
    }

    return 0;
}