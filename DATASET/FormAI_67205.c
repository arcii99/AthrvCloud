// Hello there! I am thrilled to present to you my C Natural Language Date Converter program!
// This program is designed to convert dates from English text to standard numerical format.
// Let's get started!

#include <stdio.h>
#include <string.h>

int main() {

  // Let's start by asking the user to enter the date in natural language format
  printf("Welcome to the C Natural Language Date Converter! \n");
  printf("Please enter the date in natural language format (e.g. 'January 1st, 2022'): \n");

  // We will use fgets() to read the user's input
  char date[50];
  fgets(date, 50, stdin);
  date[strcspn(date, "\n")] = 0; // Removing the newline character generated by fgets

  // Now, we will split the user's input into day, month, and year values
  char *month, *day, *year;

  // We will use the strtok() function to split the string at the comma
  month = strtok(date, " ,");
  day = strtok(NULL, " ,");
  year = strtok(NULL, " ,");

  // Converting the month value from text to number
  int monthNum;
  if (strcmp(month, "January") == 0) {
    monthNum = 1;
  } else if (strcmp(month, "February") == 0) {
    monthNum = 2;
  } else if (strcmp(month, "March") == 0) {
    monthNum = 3;
  } else if (strcmp(month, "April") == 0) {
    monthNum = 4;
  } else if (strcmp(month, "May") == 0) {
    monthNum = 5;
  } else if (strcmp(month, "June") == 0) {
    monthNum = 6;
  } else if (strcmp(month, "July") == 0) {
    monthNum = 7;
  } else if (strcmp(month, "August") == 0) {
    monthNum = 8;
  } else if (strcmp(month, "September") == 0) {
    monthNum = 9;
  } else if (strcmp(month, "October") == 0) {
    monthNum = 10;
  } else if (strcmp(month, "November") == 0) {
    monthNum = 11;
  } else if (strcmp(month, "December") == 0) {
    monthNum = 12;
  }

  // Printing the converted date in standard numerical format
  printf("The date you entered in natural language format was: %s.\n", date);
  printf("The equivalent date in standard numerical format is: %02d/%02d/%s.\n", monthNum, atoi(day), year);

  // Thanking the user for using our program
  printf("Thank you for using the C Natural Language Date Converter.\n");

  return 0; // Mission accomplished! No errors found during compilation and execution.
}