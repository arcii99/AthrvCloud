void print_board() {
    for (int i = 0; i < ROWS; i++) {
        for (int j = 0; j < COLS; j++) {
            if (board[i][j] == 1) {
                printf("#");
            } else {
                printf("-");
            }
        }
        printf("\n");
    }
}

int count_neighbors(int row, int col) {
    int count = 0;

    if (row > 0) {
        count += board[row - 1][col];
    }

    if (row < ROWS - 1) {
        count += board[row + 1][col];
    }

    if (col > 0) {
        count += board[row][col - 1];
    }

    if (col < COLS - 1) {
        count += board[row][col + 1];
    }

    if (row > 0 && col > 0) {
        count += board[row - 1][col - 1];
    }

    if (row > 0 && col < COLS - 1) {
        count += board[row - 1][col + 1];
    }

    if (row < ROWS - 1 && col > 0) {
        count += board[row + 1][col - 1];
    }

    if (row < ROWS - 1 && col < COLS - 1) {
        count += board[row + 1][col + 1];
    }

    return count;
}

void evolve() {
    int new_board[20][50];

    for (int i = 0; i < ROWS; i++) {
        for (int j = 0; j < COLS; j++) {
            int neighbors = count_neighbors(i, j);

            if (board[i][j] == 1) {
                if (neighbors < 2 || neighbors > 3) {
                    new_board[i][j] = 0;
                } else {
                    new_board[i][j] = 1;
                }
            } else {
                if (neighbors == 3) {
                    new_board[i][j] = 1;
                } else {
                    new_board[i][j] = 0;
                }
            }
        }
    }

    for (int i = 0; i < ROWS; i++) {
        for (int j = 0; j < COLS; j++) {
            board[i][j] = new_board[i][j];
        }
    }
}

