#include <stdio.h>

int main() {
    int a = 25;
    int b = 13;
    int c;

    // Bitwise AND operator
    c = a & b;
    printf("The value of a & b is %d\n", c);

    // Bitwise OR operator
    c = a | b;
    printf("The value of a | b is %d\n", c);

    // Bitwise XOR operator
    c = a ^ b;
    printf("The value of a ^ b is %d\n", c);

    // Bitwise left shift operator
    c = a << 2;
    printf("The value of a << 2 is %d\n", c);

    // Bitwise right shift operator
    c = a >> 2;
    printf("The value of a >> 2 is %d\n", c);

    printf("Isn't Bitwise operations exciting! These operators make programmers happy as it offers complete control over the bits and bytes of data!\n");

    return 0;
}