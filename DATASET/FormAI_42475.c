#include<stdio.h>

int main() {
    int a = 29;
    int b = 71;
    printf("The value of a is %d and the value of b is %d\n", a, b);

    int c = a & b;
    printf("The bitwise AND of a and b is %d\n", c);

    int d = a | b;
    printf("The bitwise OR of a and b is %d\n", d);

    int e = a ^ b;
    printf("The bitwise XOR of a and b is %d\n", e);

    int f = ~a;
    printf("The bitwise NOT of a is %d\n", f);

    int g = a << 2;
    printf("The value of a shifted left by 2 bits is %d\n", g);

    int h = b >> 1;
    printf("The value of b shifted right by 1 bit is %d\n", h);

    return 0;
}