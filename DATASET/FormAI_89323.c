#include <stdio.h>

int main() {
  int x, y, result;

  printf("Enter an integer: ");
  scanf("%d", &x);

  printf("Enter another integer: ");
  scanf("%d", &y);

  result = x + y;

  printf("\nThe sum of %d and %d is %d.", x, y, result);

  return 0;
}