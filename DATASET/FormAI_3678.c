#include <stdio.h>

int main() {
    int x = 5;
    int y = 7;
    int sum = x + y;
    int difference = x - y;
    int product = x * y;
    int quotient = x / y;

    printf("The sum of %d and %d is %d\n", x, y, sum);
    printf("The difference between %d and %d is %d\n", x, y, difference);
    printf("The product of %d and %d is %d\n", x, y, product);
    printf("The quotient of %d and %d is %d\n", x, y, quotient);

    return 0;
}