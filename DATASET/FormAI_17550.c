#include <stdio.h>

int main() {
  printf("My love for you is like a C program - complex and abstract.\n");
  printf("Let me show you how I parse my feelings with syntax: \n");

  char love[20] = "I love you";
  
  // Converting ASCII to integer values
  int i = love[0] - 'A';
  int j = love[2] - 'A';
  int k = love[3] - 'A';
  
  // Displaying the parsed values
  printf("My love for you is %d%% complex,\n", i*10);
  printf("%d%% abstract, and\n", j*10);
  printf("%d%% beautiful.\n", k*10);

  // Returning 0 to signify successful parsing
  return 0;
}