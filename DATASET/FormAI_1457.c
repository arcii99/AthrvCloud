#include <stdio.h>

int main() {
   int a = 17;
   int b = 8;
   int c = a | b;        // bitwise OR operation
   int d = a & b;        // bitwise AND operation
   int e = a ^ b;        // bitwise XOR operation
   int f = ~a;           // bitwise NOT operation
   int g = a >> 2;       // bitwise right shift operation
   int h = a << 2;       // bitwise left shift operation   

   printf("\n Let's do some happy bitwise operations!\n\n");
   printf("The value of a is %d\n", a);
   printf("The value of b is %d\n", b);

   printf("Bitwise OR operation (a | b) = %d\n", c);
   printf("Bitwise AND operation (a & b) = %d\n", d);
   printf("Bitwise XOR operation (a ^ b) = %d\n", e);
   printf("Bitwise NOT operation (~a) = %d\n", f);
   printf("Bitwise right shift operation (a >> 2) = %d\n", g);
   printf("Bitwise left shift operation (a << 2) = %d\n\n", h);

   // Let's do something creative
   printf("I am super happy because I just did some amazing bitwise operations! Woo hoo! :D\n");

   return 0;
}